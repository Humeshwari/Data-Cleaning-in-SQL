select *,
	ROW_NUMBER()OVER(
		Partition by ParcelID,
					 PropertyAddress,
					 SalePrice,
					 SaleDate,
					 LegalReference
					 ORDER BY 
					 UniqueID
					 )row_num
FROM PortfolioProject..NashvilleHousing
ORDER BY ParcelID

with RowNumCTE AS(
select *,
	ROW_NUMBER()OVER(
		Partition by ParcelID,
					 PropertyAddress,
					 SalePrice,
					 SaleDate,
					 LegalReference
					 ORDER BY 
					 UniqueID
					 )row_num
FROM PortfolioProject..NashvilleHousing
--ORDER BY ParcelID
)
SELECT *
FROM RowNumCTE
WHERE row_num>1
ORDER BY PropertyAddress


